DROP TABLE IF EXISTS `RANKED_LINEAGE`;

CREATE TABLE `RANKED_LINEAGE` (
    TAX_ID INTEGER NOT NULL,
    TAX_NAME VARCHAR(1000) NOT NULL,
    SPECIES VARCHAR(255),
    GENUS VARCHAR(255),
    FAMILY VARCHAR(255),
    `ORDER` VARCHAR(255),
    CLASS VARCHAR(255),
    PHYLUM VARCHAR(255),
    KINGDOM VARCHAR(255),
    SUPERKINGDOM VARCHAR(255)
);