DROP TABLE IF EXISTS `CITATIONS`;
DROP TABLE IF EXISTS `DELETED_NODES`;
DROP TABLE IF EXISTS `DIVISION`;
DROP TABLE IF EXISTS `EXCLUDE_FROM_TYPE`;
DROP TABLE IF EXISTS `FULL_NAME_LINEAGE`;
DROP TABLE IF EXISTS `GENCODE`;
DROP TABLE IF EXISTS `HOSTS`;
DROP TABLE IF EXISTS `IMAGE`;
DROP TABLE IF EXISTS `MERGED`;
DROP TABLE IF EXISTS `NAMES`;
DROP TABLE IF EXISTS `NODES`;
DROP TABLE IF EXISTS `RANKED_LINEAGE`;
DROP TABLE IF EXISTS `TAX_ID_LINEAGE`;
DROP TABLE IF EXISTS `TYPE_OF_MATERIAL`;
DROP TABLE IF EXISTS `TYPE_OF_TYPE`;

CREATE TABLE `CITATIONS`
(
    CIT_ID      INTEGER NOT NULL,
    CIT_KEY     VARCHAR(1000),
    MEDLINE_ID  VARCHAR(100),
    PUBMED_ID   VARCHAR(100),
    URL         VARCHAR(1000),
    TEXT        VARCHAR(5000),
    TAX_ID_LIST LONGTEXT
);

CREATE TABLE `DELETED_NODES`
(
    TAX_ID INTEGER NOT NULL
);

CREATE TABLE `DIVISION`
(
    DIVISION_ID   INTEGER       NOT NULL,
    DIVISION_CDE  CHAR(3),
    DIVISION_NAME VARCHAR(1000) NOT NULL,
    COMMENTS      VARCHAR(1000)
);

CREATE TABLE `EXCLUDE_FROM_TYPE`
(
    TAX_ID         INTEGER NOT NULL,
    NAME_TXT       VARCHAR(1000),
    PROPERTY       VARCHAR(1000),
    VOUCHER_STRAIN VARCHAR(1000)
);

CREATE TABLE `FULL_NAME_LINEAGE`
(
    TAX_ID   INTEGER       NOT NULL,
    TAX_NAME VARCHAR(1000) NOT NULL,
    LINEAGE  VARCHAR(1000)
);

CREATE TABLE `GENCODE`
(
    GENETIC_CODE_ID   INTEGER       NOT NULL,
    ABBREVIATION      VARCHAR(100),
    GENETIC_CODE_NAME VARCHAR(1000) NOT NULL,
    CDE               VARCHAR(1000),
    STARTS            VARCHAR(1000)
);

CREATE TABLE `HOSTS`
(
    TAX_ID          INTEGER NOT NULL,
    POTENTIAL_HOSTS VARCHAR(500)
);

CREATE TABLE `IMAGE`
(
    IMAGE_ID    INTEGER NOT NULL,
    IMAGE_KEY   VARCHAR(1000),
    IMAGE_URL   VARCHAR(1000),
    LICENSE     VARCHAR(200),
    ATTRIBUTION VARCHAR(1000),
    SOURCE      VARCHAR(1000),
    PROPERTIES  VARCHAR(1000),
    TAX_ID_LIST VARCHAR(1000)
);

CREATE TABLE `MERGED`
(
    OLD_TAX_ID INTEGER NOT NULL,
    NEW_TAX_ID INTEGER NOT NULL
);

CREATE TABLE `NAMES`
(
    TAX_ID      INTEGER       NOT NULL,
    NAME_TXT    VARCHAR(1000) NOT NULL,
    UNIQUE_NAME VARCHAR(255),
    NAME_CLASS  VARCHAR(255)
);

CREATE TABLE `NODES`
(
    TAX_ID                        INTEGER      NOT NULL,
    PARENT_TAX_ID                 INTEGER,
    `RANK`                        VARCHAR(255) NOT NULL,
    EMBL_CODE                     VARCHAR(10),
    DIVISION_ID                   INTEGER,
    INHERITED_DIV_FLAG            TINYINT,
    GENETIC_CODE_ID               INTEGER,
    INHERITED_GC_FLAG             TINYINT,
    MITOCHONDRIAL_GENETIC_CODE_ID INTEGER,
    INHERITED_MGC_FLAG            TINYINT,
    GENBANK_HIDDEN_FLAG           TINYINT,
    HIDDEN_SUBTREE_ROOT_FLAG      TINYINT,
    COMMENTS                      VARCHAR(1000),
    PLASTID_GENETIC_CODE_ID       INTEGER,
    INHERITED_PGC_FLAG            TINYINT,
    SPECIFIED_SPECIES             TINYINT,
    HYDROGENOSOME_GENETIC_CODE_ID INTEGER,
    INHERITED_HGC_FLAG            TINYINT
);
);

CREATE TABLE `RANKED_LINEAGE`
(
    TAX_ID       INTEGER       NOT NULL,
    TAX_NAME     VARCHAR(1000) NOT NULL,
    SPECIES      VARCHAR(255),
    GENUS        VARCHAR(255),
    FAMILY       VARCHAR(255),
    `ORDER`      VARCHAR(255),
    CLASS        VARCHAR(255),
    PHYLUM       VARCHAR(255),
    KINGDOM      VARCHAR(255),
    SUPERKINGDOM VARCHAR(255)
);

CREATE TABLE `TAX_ID_LINEAGE`
(
    TAX_ID  INTEGER NOT NULL,
    LINEAGE VARCHAR(1000)
);

CREATE TABLE `TYPE_OF_MATERIAL`
(
    TAX_ID     INTEGER NOT NULL,
    TYPE_NAME  VARCHAR(1000),
    TYPE       VARCHAR(1000),
    IDENTIFIER VARCHAR(200)
);

CREATE TABLE `TYPE_OF_TYPE`
(
    TYPE_NAME    VARCHAR(1000),
    SYNONYMS     VARCHAR(1000),
    NOMENCLATURE VARCHAR(50),
    DESCRIPTION  VARCHAR(1000)
);
